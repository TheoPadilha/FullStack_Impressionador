// Adição

let valor = 10;
let segundoValor = 20;
console.log(valor + segundoValor);

let msg = "12";
console.log(msg + valor);

// Subtração
console.log(valor - segundoValor);
console.log(segundoValor - valor);

// Divisão
console.log(valor / segundoValor);
console.log(segundoValor / valor);

// Modulo
console.log(valor % segundoValor);
console.log(segundoValor % valor);

// Operadores aritimeticos avançados

// Exponenciação
let base = 2;
let expoente = 5;
let resultado = base ** expoente;

console.log(resultado);

// Incremento ++ (Adicionar o valor 1) ; Descremento -- (diminuir o valor 1)
let controle = 0;
console.log(controle++);
console.log(controle)
// ou 
console.log(++controle)


